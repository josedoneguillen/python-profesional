# Declaracion de variable
color_luz = "amarillo"

# Condicional if / else / elif
if color_luz == "verde" :
  print("Puede continuar")
elif color_luz == "amarillo" :
  print("Alto parcial")
elif color_luz == "rojo" :
  print("Alto total")  
else :
  print("El color no existe")  


