# Declaracion de variables
calificacion = 10
color = None

# Condicional if
if calificacion >= 7 :
  color = "verde"
else :
  color = "rojo"

# Imprimir resultado
print(calificacion, color)


# Reducido

# Declaracion de variables
calificacion = 5
color = "verde" if calificacion >= 7 else "rojo"

# Imprimir resultado
print(calificacion, color)