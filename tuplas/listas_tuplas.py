# Declaracion de lista
lista = ["Curso", "Python", "CodigoFacilito"]

# Declaracion de tupla
tupla = ("Introduccion", "Basico", "Listas", "Tuplas")

# Declaracion de variable string
mensaje = "Hola"

# Cambio de tupla a lista
nueva_lista = list(tupla)

# Imprimir la nueva lista
print(nueva_lista)

# Cambio de lista a tupla
nueva_tupla = tuple(lista)

# Imprimir la nueva tupla
print(nueva_tupla)


# Cambio de string a lista
nuevo_mensaje = list(mensaje)

# Imprimir el nuevo mensaje
print(nuevo_mensaje)
