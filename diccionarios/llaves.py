# Declarar variable tipo diccionario
diccionario = {"a": 1, "b": 2, "c": 3, "d": 4, "e": 5, "f": 6, "g": 7, "h": 8}

# Almacenar llaves en la variable resultados
resultado = diccionario.keys()

# Imprimir resultados
print(resultado)

# Almacenar valores en la variable resultados
resultado = diccionario.values()

# Imprimir resultados
print(resultado)

# Almacenar items en la variable resultados
resultado = diccionario.items()

# Imprimir resultados
print(resultado)